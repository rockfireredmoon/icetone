package icetone.css;

import java.util.HashMap;
import java.util.Map;

public class CssEvent {

	private final static Map<String, CssEvent> events = new HashMap<>();

	public final static CssEvent SHOW = new CssEvent("show");
	public final static CssEvent HIDE = new CssEvent("hide");
	public final static CssEvent HIGHLIGHT = new CssEvent("highlight");

	private String event;

	public CssEvent(String event) {
		events.put(event, this);
		this.event = event;
	}

	public static CssEvent getEvent(String event) {
		return events.get(event);
	}

	@Override
	public String toString() {
		return event;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((event == null) ? 0 : event.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		CssEvent other = (CssEvent) obj;
		if (event == null) {
			if (other.event != null)
				return false;
		} else if (!event.equals(other.event))
			return false;
		return true;
	}
}